function showa()
{
	var x = document.getElementById("subitema");
	var y = document.getElementById("iIcona");
	if(x.style.display === "block")
	{
		x.style.display = "none";
		y.style.transform = "rotate(90deg)";
	}
	else
	{
		x.style.display = "block";
		y.style.transform = "rotate(0deg)";
	}
}
